alter table match_stats enable row level security;
alter table predictions enable row level security;
alter table prediction_results enable row level security;
alter table profiles enable row level security;
alter table subscriptions enable row level security;
alter table payments enable row level security;
alter table user_favorites enable row level security;
alter table audit_logs enable row level security;
alter table sync_log enable row level security;

-- 4.1 Tablas públicas (lectura para anónimos)
create policy "Leagues son públicas" on leagues for select using (true);
create policy "Teams son públicos" on teams for select using (true);
create policy "Matches son públicos" on matches for select using (true);
create policy "Match_stats son públicos" on match_stats for select using (true);
create policy "Predictions públicas" on predictions for select using (true);
create policy "Prediction_results públicos" on prediction_results for select using (true);

-- 4.2 Perfiles: cada usuario solo ve/edita su perfil
create policy "Usuarios ven su perfil" on profiles
  for select using (auth.uid() = id);
create policy "Usuarios editan su perfil" on profiles
  for update using (auth.uid() = id);
-- El insert lo hace Supabase Auth automáticamente

-- 4.3 Suscripciones: cada usuario ve las suyas
create policy "Usuarios ven sus suscripciones" on subscriptions
  for select using (auth.uid() = user_id);

-- 4.4 Pagos: cada usuario ve los suyos
create policy "Usuarios ven sus pagos" on payments
  for select using (auth.uid() = user_id);

-- 4.5 Favoritos: CRUD completo solo para el dueño
create policy "Usuarios ven sus favoritos" on user_favorites
  for select using (auth.uid() = user_id);
create policy "Usuarios crean sus favoritos" on user_favorites
  for insert with check (auth.uid() = user_id);
create policy "Usuarios eliminan sus favoritos" on user_favorites
  for delete using (auth.uid() = user_id);

-- 4.6 Audit logs: nadie los lee excepto admin via service_role
create policy "Audit logs solo service_role" on audit_logs
  for all using (false);

-- 4.7 Sync log: solo service_role
create policy "Sync_log solo service_role" on sync_log
  for all using (false);

-- ================================================================
-- 5. FUNCIÓN PARA CREAR PERFIL AUTOMÁTICAMENTE AL REGISTRARSE
-- ================================================================

create or replace function handle_new_user()
returns trigger as $$
begin
  insert into profiles (id, email, display_name, avatar_url)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'full_name', split_part(new.email, '@', 1)),
    new.raw_user_meta_data->>'avatar_url'
  );
  -- Crear suscripción free automática
  insert into subscriptions (user_id, plan, status, trial_ends_at, current_period_start, current_period_end)
  values (
    new.id,
    'free',
    'active',
    null,
    now(),
    '2099-12-31'::timestamptz
  );
  return new;
end;
$$ language plpgsql security definer;

-- Trigger que se ejecuta cuando se crea un usuario en auth.users
create or replace trigger on_auth_user_created
  after insert on auth.users
  for each row execute function handle_new_user();

